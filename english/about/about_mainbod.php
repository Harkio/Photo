    <h2>About Us</h2>
    <p>I am Graham Harkness and I have grown up in this Fair City! I am a landscape photographer but my background is also in archaeology. Not only do I offer photography tours and workshops in Dublin city, I can also tell you everything you want to know about the history of the city, the significance of the landmarks and the ethnicity of the people! Photography is my passion, and I have traveled as far as Cambodia to get the perfect shot, though I can most often be found in the mountains of Wicklow, or on the beaches of North Dublin, late at night or early in the morning capturing those long exposures!</p>
    
