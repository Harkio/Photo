  <h2 align="left">What is photo processing</h2>
    <p align="justify">So I have friends who look at my photos and say "oh Photoshop, thats just cheating". Well I have news for you, guess what? People have been processing photos since the dawn of photography in darkrooms and with chemicals to acchieve desired results. The difference today is that processing technology has advanced to the stage where you do not need a degree in chemistry and an expensive dark room set up to create beautiful images. You generally need an Adobe subscription which roughly costs 25 euro a month for Photoshop cc or Lightroom. </p> <p align="justify">Post processing is essentially altering the image after you have taken it.Most people will want to crop and straighten their images after they have taken them however you can also enhance colour, contrast and even experiment with HDR photography. Most cameras now will allow you do change the colours settings in the camera so the body itself does some image processing. When you switch your camera to landscape mode for example the greens and blues are highlighted. Most DSLR cameras now will use long exposure noise reduction to remove grain on images taken over a certain amount of time. </p>

